# ***********************************

new_txt = [s for s in txt.lower() if s.isalpha()]
print("Дифференцирование текста на отдельные буквы - ", new_txt)
print(f"Общее количество букв в тексте: {len(new_txt)}")
print(f"Общее количество слов в тексте: {len(txt.split())}")

