# Использование иснтрументария "словарь" для посчета и сортировки:

# ***********************************

d = {}
for c in new_txt:
    d[c] = d.get(c, 0) + 1
print("В алфавитном порядке: " + " ".join(sorted(d.keys())))
pairs = d.items()
pairs_sorted = sorted(pairs, key=lambda x: (-x[1], x[0]))
print("В порядке убывания частоты: ", pairs_sorted)
